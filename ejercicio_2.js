let numero_horas = parseInt(prompt("Ingrese el número de horas trabajadas: ")); //Se solicita el número de horas trabajadas
let valor_hora = parseInt(prompt("Ingrese el valor por hora: ")); //Se solicita el valor por hora

let calculo_salario_semanal = numero_horas * valor_hora; //Se calcula el salario semanal

alert("El salario semanal es de: " + calculo_salario_semanal); //Se muestra el resultado
